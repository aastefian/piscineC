int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int nr;

	i = 0;
	nr = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
			nr++;
		i++;
	}
	return (nr);
}
