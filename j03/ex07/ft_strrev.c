char	ft_strrev(char *str)
{
	int		g;
	int		d;
	int		i;
	char	aux;

	i = 0;
	while (str[i] != '\0')
		i++;
	d = i - 1;
	g = 0;
	while (g < d)
	{
		aux = str[g];
		str[g] = str[d];
		str[d] = aux;
		d--;
		g++;
	}
	return (*str);
}
